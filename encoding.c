#include "minitalk.h"

void	char_to_binary_str(char c, char *str)
{
	int		i;

	i = 6;
	while (i >= 0)
	{
		if (c % 2)
			str[i] = '1';
		else
			str[i] = '0';
		i--;
		c /= 2;
	}
	str[7] = '\0';
}
